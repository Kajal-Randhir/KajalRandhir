#creating list
my_list=[3,1,5,9,7]
print(my_list)

#inserting element at position
my_list.insert(1,4)
print(my_list)

#remove element
my_list.remove(5)
print(my_list)

#append element
my_list.append(6)
print(my_list)

#sorting the list
my_list.sort()
print(my_list)

#pop last element fom the list
my_list.pop(-1)
print(my_list)

#reverse the list
my_list.reverse()
print(my_list)

